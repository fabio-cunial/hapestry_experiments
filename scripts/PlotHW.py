#!/usr/bin/env python3
"""
Plot a De Finetti triangle from genotype count rows as a heatmap.

Usage:
  python PlotHW.py input.csv output.png
    python PlotHW.py input.csv output.png --bins 300
    python PlotHW.py input.csv output.png --bins-x 300 --bins-y 180
        python PlotHW.py input.csv output.png --title "My Plot Title"

Input CSV must have header columns: AA,AB,BB where:
  AA = hom-ref, AB = het, BB = hom-var.

This script was almost entirely generated by ChatGPT, using `PlotHW.r` as a
starting point.
"""

import math
import sys
import argparse
from typing import Tuple

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
from matplotlib.colors import LogNorm
from scipy.stats import chi2


SQRT3 = math.sqrt(3.0)


def hw_chisq_pvalue(aa: np.ndarray, ab: np.ndarray, bb: np.ndarray) -> np.ndarray:
    """Compute HWE chi-square p-values with 1 degree of freedom.

    Mirrors the spirit of HWChisqStats() used in the original R script:
    expected counts are based on estimated allele frequency from observed counts.
    """
    n = aa + ab + bb
    # p = frequency of B allele.
    p = (2.0 * bb + ab) / (2.0 * n)
    q = 1.0 - p

    exp_aa = n * q * q
    exp_ab = n * 2.0 * p * q
    exp_bb = n * p * p

    with np.errstate(divide="ignore", invalid="ignore"):
        stat = (
            (aa - exp_aa) ** 2 / exp_aa
            + (ab - exp_ab) ** 2 / exp_ab
            + (bb - exp_bb) ** 2 / exp_bb
        )

    pvals = chi2.sf(stat, df=1)

    # Match R behavior for degenerate rows such as [N,0,0], where p-value can be NaN.
    pvals = np.where(np.isfinite(pvals), pvals, np.nan)
    return pvals


def barycentric_to_xy(aa: np.ndarray, ab: np.ndarray, bb: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    """Map genotype frequencies to De Finetti triangle Cartesian coordinates.

    Vertex mapping (same orientation as original R plot):
      AA -> left corner  (-1/sqrt(3), 0)
      AB -> top corner    (0, 1)
      BB -> right corner  (1/sqrt(3), 0)
    """
    n = aa + ab + bb
    faa = aa / n
    fab = ab / n
    fbb = bb / n

    x = (-1.0 / SQRT3) * faa + 0.0 * fab + (1.0 / SQRT3) * fbb
    y = 0.0 * faa + 1.0 * fab + 0.0 * fbb
    return x, y


def in_triangle_mask(x: np.ndarray, y: np.ndarray) -> np.ndarray:
    """Return mask of points inside (or on edge of) the triangle."""
    # Triangle edges:
    # left  edge through (-1/sqrt(3),0) and (0,1):  y = sqrt(3)*x + 1
    # right edge through ( 1/sqrt(3),0) and (0,1):  y = -sqrt(3)*x + 1
    # base edge: y = 0
    left_edge = SQRT3 * x + 1.0
    right_edge = -SQRT3 * x + 1.0
    return (
        (y >= -1e-12)
        & (y <= 1.0 + 1e-12)
        & (y <= left_edge + 1e-12)
        & (y <= right_edge + 1e-12)
    )


def make_heatmap(
    x: np.ndarray,
    y: np.ndarray,
    bins_x: int = 220,
    bins_y: int = 220,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """Build a 2D histogram and mask bins outside triangle."""
    x_min = -1.0 / SQRT3
    x_max = 1.0 / SQRT3
    y_min = 0.0
    y_max = 1.0

    heat, xedges, yedges = np.histogram2d(
        x,
        y,
        bins=[bins_x, bins_y],
        range=[[x_min, x_max], [y_min, y_max]],
    )

    # histogram2d gives [xbin, ybin]; pcolormesh expects [ybin, xbin].
    heat = heat.T

    xc = 0.5 * (xedges[:-1] + xedges[1:])
    yc = 0.5 * (yedges[:-1] + yedges[1:])
    xx, yy = np.meshgrid(xc, yc)
    inside = in_triangle_mask(xx, yy)

    heat = np.where(inside, heat, np.nan)
    return heat, xedges, yedges, inside


def _chisq_stat_from_freq(p_b: float, f_ab: float) -> float:
    """Chi-square HWE statistic from allele-B freq and heterozygote freq."""
    f_bb = p_b - 0.5 * f_ab
    f_aa = 1.0 - p_b - 0.5 * f_ab
    if f_aa < 0.0 or f_ab < 0.0 or f_bb < 0.0:
        return math.inf

    q = 1.0 - p_b
    exp_aa = q * q
    exp_ab = 2.0 * p_b * q
    exp_bb = p_b * p_b

    stat = 0.0
    if exp_aa > 0.0:
        stat += (f_aa - exp_aa) ** 2 / exp_aa
    if exp_ab > 0.0:
        stat += (f_ab - exp_ab) ** 2 / exp_ab
    if exp_bb > 0.0:
        stat += (f_bb - exp_bb) ** 2 / exp_bb
    return stat


def _bisect_root(func, lo: float, hi: float, steps: int = 50) -> float:
    """Bisection for a sign-changing interval [lo, hi]."""
    f_lo = func(lo)
    f_hi = func(hi)
    if f_lo == 0.0:
        return lo
    if f_hi == 0.0:
        return hi
    for _ in range(steps):
        mid = 0.5 * (lo + hi)
        f_mid = func(mid)
        if f_mid == 0.0:
            return mid
        if f_lo * f_mid <= 0.0:
            hi = mid
            f_hi = f_mid
        else:
            lo = mid
            f_lo = f_mid
    return 0.5 * (lo + hi)


def compute_hwe_boundary_curves(alpha: float, n_samples: int, n_grid: int = 500) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """Return lower/upper De Finetti boundary branches for a p-value threshold."""
    crit = chi2.isf(alpha, df=1) / float(n_samples)
    p_vals = np.linspace(0.0, 1.0, n_grid)

    x_low = []
    y_low = []
    x_up = []
    y_up = []

    for p_b in p_vals:
        h0 = 2.0 * p_b * (1.0 - p_b)
        h_max = 2.0 * min(p_b, 1.0 - p_b)

        def g(h: float) -> float:
            return _chisq_stat_from_freq(p_b, h) - crit

        # Lower branch: h in [0, h0]
        g_lo = g(0.0)
        g_h0 = g(h0)
        if g_lo * g_h0 <= 0.0:
            h_low = _bisect_root(g, 0.0, h0)
            f_bb = p_b - 0.5 * h_low
            f_aa = 1.0 - p_b - 0.5 * h_low
            if f_aa >= -1e-12 and f_bb >= -1e-12:
                x, y = barycentric_to_xy(np.array([f_aa]), np.array([h_low]), np.array([f_bb]))
                x_low.append(float(x[0]))
                y_low.append(float(y[0]))

        # Upper branch: h in [h0, h_max]
        g_hi = g(h_max)
        if g_h0 * g_hi <= 0.0:
            h_up = _bisect_root(g, h0, h_max)
            f_bb = p_b - 0.5 * h_up
            f_aa = 1.0 - p_b - 0.5 * h_up
            if f_aa >= -1e-12 and f_bb >= -1e-12:
                x, y = barycentric_to_xy(np.array([f_aa]), np.array([h_up]), np.array([f_bb]))
                x_up.append(float(x[0]))
                y_up.append(float(y[0]))

    return np.array(x_low), np.array(y_low), np.array(x_up), np.array(y_up)


def add_self_check_overlay(ax: plt.Axes) -> None:
    """Overlay canonical points and the HWE curve to validate geometry."""
    # Canonical vertices and a balanced HWE interior point.
    check_labels = ["AA=1", "AB=1", "BB=1", "1:2:1"]
    check_aa = np.array([1.0, 0.0, 0.0, 1.0])
    check_ab = np.array([0.0, 1.0, 0.0, 2.0])
    check_bb = np.array([0.0, 0.0, 1.0, 1.0])
    cx, cy = barycentric_to_xy(check_aa, check_ab, check_bb)

    ax.scatter(cx, cy, s=26, facecolor="white", edgecolor="black", linewidth=0.8, zorder=6)
    for i, label in enumerate(check_labels):
        ax.text(cx[i], cy[i] + 0.02, label, ha="center", va="bottom", fontsize=7, color="black", zorder=7)

    # HWE curve parameterized by B-allele frequency p in [0, 1].
    p = np.linspace(0.0, 1.0, 401)
    hwe_aa = (1.0 - p) ** 2
    hwe_ab = 2.0 * p * (1.0 - p)
    hwe_bb = p**2
    hx, hy = barycentric_to_xy(hwe_aa, hwe_ab, hwe_bb)
    ax.plot(hx, hy, color="white", linewidth=1.4, alpha=0.95, zorder=5)
    ax.plot(hx, hy, color="black", linewidth=0.5, alpha=0.9, zorder=6)


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Plot a De Finetti triangle heatmap from AA/AB/BB genotype count rows."
    )
    parser.add_argument("input_csv", help="Input CSV with AA, AB, BB columns")
    parser.add_argument("output_png", help="Output PNG path")
    parser.add_argument(
        "--bins",
        type=int,
        default=220,
        help="Number of bins for both x and y (default: 220)",
    )
    parser.add_argument(
        "--bins-x",
        type=int,
        default=None,
        help="Number of bins along x axis (overrides --bins)",
    )
    parser.add_argument(
        "--bins-y",
        type=int,
        default=None,
        help="Number of bins along y axis (overrides --bins)",
    )
    parser.add_argument(
        "--self-check",
        action="store_true",
        help="Overlay known reference points and HWE curve for geometry validation",
    )
    parser.add_argument(
        "--title",
        type=str,
        default=None,
        help="Optional plot title",
    )

    args = parser.parse_args()
    input_csv = args.input_csv
    output_png = args.output_png
    bins_x = args.bins_x if args.bins_x is not None else args.bins
    bins_y = args.bins_y if args.bins_y is not None else args.bins

    if bins_x <= 0 or bins_y <= 0:
        print("--bins, --bins-x and --bins-y must be positive integers.", file=sys.stderr)
        return 1

    df = pd.read_csv(input_csv)
    required = ["AA", "AB", "BB"]
    missing = [c for c in required if c not in df.columns]
    if missing:
        print(f"Missing required columns: {', '.join(missing)}", file=sys.stderr)
        return 1

    # Keep finite, non-negative rows that contain at least one positive count.
    mat = df[required].to_numpy(dtype=float)
    valid = np.isfinite(mat).all(axis=1) & (mat >= 0).all(axis=1) & np.any(mat > 0, axis=1)
    aa = mat[valid, 0]
    ab = mat[valid, 1]
    bb = mat[valid, 2]
    n = aa + ab + bb

    if aa.size == 0:
        print("No valid rows with at least one positive AA, AB, or BB count were found.", file=sys.stderr)
        return 1

    pvals = hw_chisq_pvalue(aa, ab, bb)
    m = aa.size
    bonf = 0.05 / m

    nan_flags = np.isnan(pvals)
    n_pass = int(np.sum((pvals >= 0.05) | nan_flags))
    n_nom = int(np.sum((pvals < 0.05) & (pvals >= bonf)))
    n_bonf = int(np.sum(pvals < bonf))

    # Boundary curves assume a fixed sample size; use median per-row N as representative.
    n_rep = int(np.round(np.median(n)))
    n_rep = max(1, n_rep)

    nom_low_x, nom_low_y, nom_up_x, nom_up_y = compute_hwe_boundary_curves(alpha=0.05, n_samples=n_rep)
    bonf_low_x, bonf_low_y, bonf_up_x, bonf_up_y = compute_hwe_boundary_curves(alpha=bonf, n_samples=n_rep)

    x, y = barycentric_to_xy(aa, ab, bb)

    fig, ax = plt.subplots(figsize=(6, 6), dpi=300)

    x_min = -1.0 / SQRT3
    x_max = 1.0 / SQRT3
    y_min = 0.0
    y_max = 1.0
    hb = ax.hexbin(
        x,
        y,
        gridsize=max(bins_x, bins_y),
        extent=(x_min, x_max, y_min, y_max),
        cmap="viridis",
        mincnt=1,
        linewidths=0.0,
        edgecolors="none",
        antialiased=False,
        zorder=2,
    )

    counts = hb.get_array()
    if counts.size > 0:
        hb.set_norm(LogNorm(vmin=1.0, vmax=float(np.max(counts))))

    # cbar = fig.colorbar(hb, ax=ax, pad=0.02)
    # cbar.set_label("Records per bin")

    # Triangle boundary.
    tri_x = np.array([-1.0 / SQRT3, 0.0, 1.0 / SQRT3, -1.0 / SQRT3])
    tri_y = np.array([0.0, 1.0, 0.0, 0.0])
    ax.plot(tri_x, tri_y, color="lightgray", linewidth=1.0, zorder=0)

    # HWE significance boundaries: nominal (outer) and Bonferroni (inner).
    if nom_low_x.size > 1:
        ax.plot(nom_low_x, nom_low_y, color="lightgray", linewidth=1, linestyle="-", zorder=8)
        ax.plot(nom_up_x, nom_up_y, color="lightgray", linewidth=1, linestyle="-", zorder=8)
        # ax.plot(nom_low_x, nom_low_y, color="#F4A261", linewidth=1.2, linestyle="--", zorder=8)
        # ax.plot(nom_up_x, nom_up_y, color="#F4A261", linewidth=1.2, linestyle="--", zorder=8)
    # if bonf_low_x.size > 1:
    #     ax.plot(bonf_low_x, bonf_low_y, color="#D62828", linewidth=1.2, linestyle="-", zorder=9)
    #     ax.plot(bonf_up_x, bonf_up_y, color="#D62828", linewidth=1.2, linestyle="-", zorder=9)

    if args.self_check:
        add_self_check_overlay(ax)

    # Vertex labels placed just inside the triangle to avoid padded whitespace.
    # ax.text(-1.0 / SQRT3, 0.01, "REF", ha="center", va="bottom")
    # ax.text(1.0 / SQRT3, 0.01, "HOM", ha="center", va="bottom")
    # ax.text(0.0, 0.99, "HET", ha="center", va="top")

    ax.set_xlim(x_min, x_max)
    ax.set_ylim(y_min, y_max)
    ax.set_aspect("equal", adjustable="box")
    ax.margins(x=0, y=0)
    ax.set_xticks([])
    ax.set_yticks([])
    if args.title:
        ax.set_title(args.title + f", {100.0 * n_pass / m:.2f}% of records in HWE.", fontsize=16)
    for spine in ax.spines.values():
        spine.set_visible(False)

    print(f"SV in H-W equilibrium (n={n_pass:,}; {100.0 * n_pass / m:.2f}%)")
    print(f"SV not in H-W equilibrium (Nominal; n={n_nom:,}; {100.0 * n_nom / m:.2f}%)")
    print(f"SV not in H-W equilibrium (Bonferroni; n={n_bonf:,}; {100.0 * n_bonf / m:.2f}%)")
    print(f"n={m:,}")

    fig.subplots_adjust(left=0, right=1, top=1, bottom=0)
    fig.savefig(output_png, bbox_inches='tight', pad_inches=2.0 / fig.dpi)
    plt.close(fig)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
